



    # Operação de Comparação 



# São utilizados para comparar dois valores, por exemplo:     Valor A ~~~ Valor B 


 
saldo = 200 
saque = 200

print(saldo == saque) # Igualdade
print(saldo != saque) # Diferente ou igual 
print(saldo > saque) # maior que 
print(saldo >= saque) # maior que ou igual 
print(saldo < saque) # menor que 
print(saldo <= saque) # menor ou igual 

